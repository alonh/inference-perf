# ---------------------------------------------------------------------------
# NodeOutputRegistry — shared cross-process output store
# ---------------------------------------------------------------------------

class NodeOutputRegistry:
    """Thread- and process-safe registry mapping node_id → actual output text.

    Backed by a multiprocessing.Manager().dict() so all worker processes share
    the same store. The manager must be started before the workers are forked.

    Usage:
        manager = mp.Manager()
        registry = NodeOutputRegistry(manager)
        # pass registry to OTelTraceReplayDataGenerator
        # workers call registry.record(node_id, text) after each LLM call
        # load_lazy_data calls registry.require(node_id) to get the output
    """

    def __init__(self, manager: Optional[SyncManager] = None) -> None:
        if manager is not None:
            # Shared across processes via manager proxy
            self._node_output_text: Dict[str, str] = manager.dict()  # type: ignore[assignment]
            self._node_input_messages: Dict[str, Any] = manager.dict()  # type: ignore[assignment]
        else:
            # Single-process fallback (e.g., tests, num_workers=0)
            self._node_output_text = {}
            self._node_input_messages = {}

    def record(self, node_id: str, output_text: str, messages=None) -> None:
        """Register the actual output text for a completed node.

        Args:
            node_id: The node ID
            output_text: The actual generated output
            messages: The input messages
        """
        self._node_output_text[node_id] = output_text
        if messages:
            self._node_input_messages[node_id] = list(messages)  # Convert to list for storage

    def get(self, node_id: str) -> Optional[str]:
        """Return the output text for node_id, or None if not yet registered."""
        return self._node_output_text.get(node_id)

    def get_output_by_node_id(self, node_id: str) -> Optional[str]:
        """Return the output text for a given node_id."""
        return self._node_output_text.get(node_id)
    
    def get_messages_by_node_id(self, node_id: str) -> Optional[List[Any]]:
        """Return the input messages for a given node_id.
        
        Args:
            node_id: The node ID to look up
            
        Returns:
            List of input messages, or None if not found
        """
        return self._node_input_messages.get(node_id)
    
    def get_node_ids(self) -> List[str]:
        """Return all registered node IDs.
        
        Returns:
            List of all node IDs that have registered outputs
        """
        return list(self._node_output_text.keys())

    def require(self, node_id: str, timeout_sec: float = 30.0) -> str:
        """Return the output text for node_id, waiting if not yet registered.

        This is called when building the messages for a call that has an "output"
        segment sourced from node_id. If the predecessor has not yet completed,
        this method polls the registry until the output becomes available or timeout.

        Args:
            node_id: The node ID whose output is required
            timeout_sec: Maximum time to wait for the output (default: 30s)

        Returns:
            The output text from the predecessor node

        Raises:
            TimeoutError: If the output is not available within timeout_sec
        """
        start_time = time.time()
        poll_interval = 0.01  # 10ms initial poll interval
        max_poll_interval = 0.5  # Max 500ms between polls
        last_log_time = start_time

        while True:
            output = self._node_output_text.get(node_id)
            if output is not None:
                elapsed = time.time() - start_time
                if elapsed > 0.1:  # Log if we had to wait
                    logger.debug(f"Got output for node {node_id} after {elapsed:.2f}s wait")
                return output

            elapsed = time.time() - start_time
            
            # Log every 2 minute while waiting
            if elapsed - (last_log_time - start_time) >= 120.0:
                logger.warning(
                    f"Still waiting for output from node '{node_id}' ({elapsed:.1f}s elapsed)"
                )
                last_log_time = time.time()
            
            if elapsed >= timeout_sec:
                raise TimeoutError(
                    f"NodeOutputRegistry: output for node '{node_id}' not available after "
                    f"{timeout_sec:.1f}s. This means the predecessor call has not completed "
                    f"within the timeout period. Check that predecessor_node_ids are correct "
                    f"and that the predecessor is not blocked or failed."
                )

            # Exponential backoff for polling
            time.sleep(min(poll_interval, max_poll_interval))
            poll_interval *= 1.5

    def wait_for_all(self, node_ids: List[str], timeout_sec: float = 30.0) -> None:
        """Wait for all specified nodes to have registered outputs.

        Args:
            node_ids: List of node IDs to wait for
            timeout_sec: Maximum time to wait for all outputs (default: 30s)

        Raises:
            TimeoutError: If any output is not available within timeout_sec
        """
        if not node_ids:
            return
        
        logger.debug(f"Waiting for {len(node_ids)} predecessor(s): {node_ids}")
        for node_id in node_ids:
            self.require(node_id, timeout_sec=timeout_sec)
        logger.debug(f"All {len(node_ids)} predecessor(s) completed")


# ---------------------------------------------------------------------------
# OTelChatCompletionAPIData — captures output and registers it in the registry
# ---------------------------------------------------------------------------

class OTelChatCompletionAPIData(ChatCompletionAPIData):
    """ChatCompletionAPIData subclass for OTel trace replay.

    Overrides process_response to:
    1. Extract the actual output text from the LLM response.
    2. Register it in the NodeOutputRegistry under this call's node_id.
    3. Return OTelInferenceInfo (InferenceInfo subclass) with output_text populated.

    The registry and node_id are passed at construction time via Pydantic fields.
    arbitrary_types_allowed is required because NodeOutputRegistry is not a Pydantic model.
    
    Dependency handling:
    - predecessor_node_ids: List of nodes that must complete before this one
    - wait_ms: Additional delay after predecessors complete
    - input_segments: Segment decomposition for output substitution
    - original_messages: Original messages before substitution (used as template)
    """

    model_config = {"arbitrary_types_allowed": True}

    node_id: str
    registry: NodeOutputRegistry
    
    # Dependency information for async waiting
    predecessor_node_ids: List[str] = field(default_factory=list)
    wait_ms: int = 0
    input_segments: List[InputSegment] = field(default_factory=list)
    original_messages: List[Dict[str, Any]] = field(default_factory=list)
    expected_output_content: Optional[str] = None
    
    # Completion callback for graph traversal
    completion_callback: Optional[Callable[[str, float], None]] = None
    
    async def wait_for_predecessors_and_substitute(self) -> None:
        """Wait for predecessors asynchronously and substitute output segments.
        
        This method is called by the worker before dispatching the HTTP request.
        It ensures that all predecessor nodes have completed and their outputs
        are available, then substitutes "output" segments with actual predecessor
        outputs instead of the recorded assistant messages.
        
        The waiting is done in an executor to avoid blocking the event loop.
        """
        import asyncio
        
        if self.predecessor_node_ids:
            logger.debug(
                f"Node {self.node_id} waiting for {len(self.predecessor_node_ids)} predecessor(s): "
                f"{self.predecessor_node_ids}"
            )
            # Run blocking wait in executor to avoid blocking event loop
            loop = asyncio.get_event_loop()
            await loop.run_in_executor(
                None,
                self.registry.wait_for_all,
                self.predecessor_node_ids,
                3600.0  # 1 hour timeout
            )
            logger.debug(f"Node {self.node_id} predecessors completed")
        
        # Wait additional delay specified in wait_ms
        if self.wait_ms > 0:
            wait_sec = self.wait_ms / 1000.0
            logger.debug(f"Node {self.node_id} waiting {wait_sec:.3f}s (wait_ms={self.wait_ms})")
            await asyncio.sleep(wait_sec)
        
        # Substitute output segments with actual predecessor outputs and also the shared segments after substitution
        if any(seg.type == "output" or seg.type == "shared" for seg in self.input_segments):
            logger.debug(f"Node {self.node_id} substituting output/shared segments")
            substituted = self._build_messages_with_substitution()
            # Update messages with substituted content
            self.messages = [
                ChatMessage(role=m["role"], content=m["content"])
                for m in substituted
            ]
            logger.debug(f"Node {self.node_id} substitution complete, {len(self.messages)} messages")

    def _build_messages_with_substitution(self) -> List[Dict[str, Any]]:
        """Build messages with output segments substituted with actual predecessor outputs.

        For assistant messages, we substitute their output messages with the actual output.
        """
        if not self.input_segments:
            return self.original_messages

        result = []
        cursor = 0

        for seg in self.input_segments:
            seg_msgs = self.original_messages[cursor:cursor + seg.message_count]

            if seg.type == "output":
                # Output segment: substitute with actual output from the specific predecessor
                if seg.source_node_id:
                    actual_output = self.registry.get_output_by_node_id(seg.source_node_id)
                    logger.debug(
                        f"Registry get for node {self.node_id} output segment from {seg.source_node_id} generated: {actual_output}")

                    if actual_output:
                        for msg in seg_msgs:
                            substituted = dict(msg)
                            substituted["content"] = actual_output
                            result.append(substituted)
                        logger.debug(
                            f"Node {self.node_id}: substituted output segment "
                            f"with actual output from {seg.source_node_id}"
                        )
                    else:
                        logger.warning(
                            f"Node {self.node_id}: output segment from {seg.source_node_id} "
                            f"not available, using recorded content"
                        )
                        result.extend(seg_msgs)
                else:
                    logger.warning(
                        f"Node {self.node_id}: output segment has no source_node_id, "
                        f"using recorded content"
                    )
                    result.extend(seg_msgs)
            elif seg.type == "shared":
                seg_msgs_from_parent = self.registry.get_messages_by_node_id(seg.source_node_id)
                logger.debug(f"Registry get for node {self.node_id} from {seg.source_node_id} shared segment from {seg.source_node_id} num messages in parent node: {len(seg_msgs_from_parent)}")
                if len(seg_msgs_from_parent) != len(seg_msgs):
                    logger.error(f"***************** node {self.node_id} shared segment from {seg.source_node_id} had different number of messages in parent node: {len(seg_msgs_from_parent)}, num messages in seg: {len(seg_msgs)}")
                for msg_idx, msg in enumerate(seg_msgs_from_parent):
                    result.append(dict(msg))
            elif seg.type == "unique":
                # Unique message, keep as-is
                for msg_idx, msg in enumerate(seg_msgs):
                    result.append(msg)
            else:
                # Unknown segment type, keep as-is
                result.extend(seg_msgs)

            cursor += seg.message_count

        return result

    
    def on_completion(self, info: InferenceInfo) -> None:
        """Called after every request completes (real or mock) to register output and unblock successors."""
        output_text = info.output_text if isinstance(info, OTelInferenceInfo) else ""
        output_text = output_text or ""
        self.registry.record(self.node_id, output_text, self.messages)
        logger.debug(f"calling registry record for node {self.node_id} num input messages {len(self.messages)} and output: {output_text}")
        if self.completion_callback:
            logger.debug(f"Calling completion callback for node {self.node_id}")
            self.completion_callback(self.node_id, time.perf_counter())
            logger.debug(f"Completion callback finished for node {self.node_id}")
        else:
            logger.warning(f"No completion callback set for node {self.node_id}")


    async def process_response(
        self,
        response: ClientResponse,
        config: APIConfig,
        tokenizer: CustomTokenizer,
        lora_adapter: Optional[str] = None,
    ) -> OTelInferenceInfo:
        """Process the LLM response, capture output text, and register it."""
        logger.debug(f"process_response called for node {self.node_id}")
        output_text: str = ""

        if config.streaming:
            output_text = ""
            output_token_times: List[float] = []
            buffer = b""
            async for chunk in response.content.iter_any():
                buffer += chunk
                while b"\n\n" in buffer:
                    message, buffer = buffer.split(b"\n\n", 1)
                    output_token_times.append(time.perf_counter())
                    for line in message.split(sep=b"\n"):
                        if line.startswith(b"data:"):
                            data_str = line.removeprefix(b"data: ").strip()
                            if data_str == b"[DONE]":
                                break
                            try:
                                data = json.loads(data_str)
                                choices = data.get("choices", [])
                                if choices:
                                    delta = choices[0].get("delta", {})
                                    content = delta.get("content")
                                    if content:
                                        output_text += content
                            except (json.JSONDecodeError, IndexError):
                                continue
                    else:
                        continue
                    break

            prompt_text = "".join([msg.content for msg in self.messages if msg.content])
            prompt_len = tokenizer.count_tokens(prompt_text)
            output_len = tokenizer.count_tokens(output_text)
            info = OTelInferenceInfo(
                input_tokens=prompt_len,
                output_tokens=output_len,
                output_token_times=output_token_times,
                lora_adapter=lora_adapter,
                output_text=output_text or None,
            )
        else:
            data = await response.json()
            prompt_len = tokenizer.count_tokens("".join([m.content for m in self.messages]))
            choices = data.get("choices", [])
            if choices:
                output_text = "".join(
                    [choice.get("message", {}).get("content", "") for choice in choices]
                )
            output_len = tokenizer.count_tokens(output_text)
            info = OTelInferenceInfo(
                input_tokens=prompt_len,
                output_tokens=output_len,
                lora_adapter=lora_adapter,
                output_text=output_text or None,
            )

        # Register output and notify successors.
        self.on_completion(info)

        if output_text:
            logger.debug(
                f"Registered output for node {self.node_id}: {len(output_text)} chars : {output_text}"
            )
        else:
            logger.debug(f"Registered empty output for node {self.node_id}")

        return info


